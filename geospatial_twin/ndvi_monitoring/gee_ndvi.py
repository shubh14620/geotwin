    # ──────────────────────────────────────────────────────────────────────────
    def save_script(self, filepath: str = "gee_ndvi_script.js"):
        """Save GEE JavaScript to a .js file."""
        script = self.build_gee_script()
        with open(filepath, "w") as f:
            f.write(script)
        logger.info(f"GEE NDVI script saved: {filepath}")
