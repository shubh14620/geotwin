    # ──────────────────────────────────────────────────────────────────────────
    def save_script(self, filepath: str = "gee_flood_script.js"):
        """Save the GEE JavaScript script to a file for use in Code Editor."""
        script = self.build_gee_script()
        with open(filepath, "w") as f:
            f.write(script)
        logger.info(f"GEE script saved to: {filepath}")
